#On retourne un string puis on affiche le résultat booléen de l'inégalité. 5 est supérieur -2 alors il renvoie True et il l'affiche à l'écran grâce au puts
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"

#On retourne un string puis on affiche le résultat booléen de l'inégalité. 5 est supérieur OU égal -2 alors il renvoie True et il l'affiche à l'écran grâce au puts
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
#On retourne un string puis on affiche le résultat booléen de l'inégalité. 5 est inférieur OU égal à -2 alors il renvoie False et il l'affiche à l'écran grâce au puts
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"









