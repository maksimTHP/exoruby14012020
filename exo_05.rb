#On retourne un string, pas de calcul car par de crochets, tout est pris sous forme de string
puts "Est ce que c'est vrai que 3 + 2 < 5 - 7 ?"

#On affiche le résultat booléen de l'inégalité. 3 +2 n'est pas inférieur à 5 - 7 alors il renvoie False et il l'affiche à l'écran grâce au puts
puts 3 + 2 < 5 - 7

#L18/19 On retourne un string et dans cette même string on fait un calcul entre les crochets.
puts "ca fait combien 3 + 2 ? #{3 + 2}"
puts "Ca fait combien 5 - 7 ? #{5 - 7}"

# L22/23 On retourne un string
